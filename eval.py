# Import Python's built-in JSON module for reading and writing structured data files.
# It is used to load the evaluation dataset and save the final results.
import json

# Import the main agent function that will be evaluated on each topic in the dataset.
# The agent is expected to return a report or structured answer for the given topic.
from agent import run_agent


# Compute a simple keyword coverage score between a generated answer and a list of expected keywords.
# The score is the fraction of keywords that appear in the answer text.
def keyword_score(text, keywords):
    # Convert the text to a lowercase string so matching is case-insensitive.
    text = str(text).lower()

    # Keep track of how many expected keywords are found in the text.
    found = 0

    # Check each keyword one by one.
    for keyword in keywords:
        # If the keyword appears anywhere in the text, count it as found.
        if keyword.lower() in text:
            found += 1

    # Return the ratio of found keywords to the total number of keywords.
    # max(len(keywords), 1) avoids division by zero when the keyword list is empty.
    return found / max(len(keywords), 1)


# Measure retrieval quality by checking whether the system retrieved the expected source files.
# The function compares filenames found in evidence_map against the expected list of source filenames.
def retrieval_score(evidence_map, expected_sources):
    # Use a set to store unique filenames retrieved across all evidence entries.
    retrieved_files = set()

    # Iterate over each group of evidences in the evidence map.
    for evidences in evidence_map.values():
        # Inspect each evidence item in the group.
        for e in evidences:
            # Extract only the filename part from the file path.
            # This normalizes paths across operating systems.
            filename = e["file"].replace("\\", "/").split("/")[-1]
            retrieved_files.add(filename)

    # Convert the expected source list into a set for efficient comparison.
    expected_files = set(expected_sources)

    # Count how many expected files were actually retrieved.
    matched = len(expected_files.intersection(retrieved_files))

    # Return the fraction of expected files that were successfully retrieved.
    return matched / max(len(expected_files), 1)


# Measure faithfulness by checking whether words from the final answer appear in the retrieved evidence snippets.
# This is a rough approximation of how closely the answer stays grounded in the evidence.
def faithfulness_score(answer, evidences):
    # Combine all evidence snippets into one lowercase text block for easier comparison.
    evidence_text = " ".join(
        e.get("snippet", "").lower()
        for e in evidences
    )

    # Split the answer into words and convert them into a set to remove duplicates.
    answer_words = set(str(answer).lower().split())

    # Count how many unique answer words are present in the evidence text.
    matched = 0
    for word in answer_words:
        if word in evidence_text:
            matched += 1

    # Return the proportion of answer words supported by the evidence.
    return matched / max(len(answer_words), 1)


# Open the evaluation dataset file containing multiple test cases.
# Each case is expected to include a topic, expected keywords, and expected source files.
with open("evaluation_dataset.json", "r", encoding="utf-8") as f:
    dataset = json.load(f)


# Store per-topic evaluation results here before writing them to disk.
results = []


# Loop through each test case in the dataset.
for case in dataset:
    # Print the topic currently being evaluated so the user can track progress.
    print(f"Running: {case['topic']}")

    # Run the agent on the current topic and collect its output.
    result = run_agent(case["topic"])

    # If the agent returns a dictionary, try to extract the main report field.
    # Different agents may use different key names, so several options are checked.
    if isinstance(result, dict):
        report = result.get("report") or result.get("answers") or json.dumps(result, ensure_ascii=False)
    else:
        # If the result is not a dictionary, convert it into a string directly.
        report = str(result)

    # Load the evidence file generated by the system for the current run.
    # This file is expected to contain retrieval evidence grouped by question or subtask.
    with open("output/evidence.json", "r", encoding="utf-8") as f:
        evidence_map = json.load(f)

    # Calculate the keyword-based accuracy score for the report.
    # This checks whether expected terms appear in the generated answer.
    accuracy = keyword_score(
        report,
        case["expected_keywords"]
    )

    # Calculate the retrieval score by comparing retrieved files against expected source files.
    retrieval = retrieval_score(
        evidence_map,
        case["expected_sources"]
    )

    # Store faithfulness scores for each evidence group before averaging them.
    faithfulness_scores = []

    # Evaluate how well the report is grounded in each evidence set.
    for question, evidences in evidence_map.items():
        score = faithfulness_score(
            report,
            evidences
        )
        faithfulness_scores.append(score)

    # Compute the average faithfulness score across all evidence groups.
    # If no evidence exists, default to 0 to avoid division by zero.
    faithfulness = (
        sum(faithfulness_scores) / len(faithfulness_scores)
        if faithfulness_scores else 0
    )

    # Print the three evaluation metrics for the current test case.
    print("Accuracy =", accuracy)
    print("Retrieval =", retrieval)
    print("Faithfulness =", faithfulness)

    # Store the scores in a structured results list for later export.
    results.append({
        "topic": case["topic"],
        "accuracy_score": accuracy,
        "retrieval_score": retrieval,
        "faithfulness_score": faithfulness
    })


# Save all evaluation results into a JSON file for later analysis.
# indent=2 makes the file easy to read and inspect.
with open("evaluation_results.json", "w", encoding="utf-8") as f:
    json.dump(results, f, indent=2)


# Notify the user that evaluation has finished successfully.
print("Evaluation completed.")